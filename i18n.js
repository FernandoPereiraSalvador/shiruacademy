// ==== Función para aplicar traducciones en la página ====

function getBaseLocale() {
  const lang = (navigator.language || navigator.userLanguage || "es-ES").toLowerCase();

  if (lang.startsWith("es")) return "es-ES";
  if (lang.startsWith("en")) return "en-GB";
  if (lang.startsWith("de")) return "de-DE";
  if (lang.startsWith("fr")) return "fr-FR";
  if (lang.startsWith("ko")) return "ko-KR";
  if (lang.startsWith("zh")) return "zh-CN";

  // Por defecto, inglés
  return "en-GB";
}

function applyTranslations(page) {
  const baseLocale = getBaseLocale();
  const localeData = translations[baseLocale] && translations[baseLocale][page];

  // Fallback a inglés si no existe el idioma
  const dict =
    localeData ||
    (translations["en-GB"] && translations["en-GB"][page]) ||
    null;

  if (!dict) return;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });
}
