const supportedLanguages = ['es','en','fr','de','zh','ko'];

function detectLang(){
  const lang = (navigator.languages && navigator.languages[0]) || navigator.language || 'en';
  const lower = lang.toLowerCase();
  return supportedLanguages.find(l => lower.startsWith(l)) || 'en';
}

